-- EXEMPLO 02


SELECT COD_CONTRATO, DATA, TOTAL, 'UNION 1' AS QUERY 
FROM TCONTRATO 
WHERE TOTAL >= 1000 AND TOTAL < 2000

UNION ALL

SELECT COD_CONTRATO, DATA, TOTAL, 'UNION 2' AS QUERY 
FROM TCONTRATO 
WHERE DESCONTO IS NOT NULL

UNION

SELECT COD_CONTRATO, DATA, TOTAL, 'UNION 3' AS QUERY 
FROM TCONTRATO 
WHERE TOTAL > 2000

ORDER BY 4;


-- EXEMPLO 03


SELECT COD_CONTRATO, DATA, DESCONTO, TOTAL
FROM TCONTRATO 
WHERE DESCONTO IS NOT NULL

INTERSECT

SELECT COD_CONTRATO, DATA, DESCONTO, TOTAL 
FROM TCONTRATO 
WHERE TOTAL > 1000

ORDER BY 1;


-- EXEMPLO 04



SELECT COD_CONTRATO, DATA, DESCONTO, TOTAL
FROM TCONTRATO 
WHERE DESCONTO IS NOT NULL

MINUS

SELECT COD_CONTRATO, DATA, DESCONTO, TOTAL 
FROM TCONTRATO 
WHERE TOTAL > 2000

ORDER BY 1;
